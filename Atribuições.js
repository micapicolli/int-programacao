let x = 10; // Inicializa a variável x com o valor 10
console.log(`Valor inicial de x: ${x}`);

x += 5;    // Atribuição de adição: x = x + 5 → 10 + 5 = 15
console.log(`Após x += 5: ${x}`);

x -= 3;    // Atribuição de subtração: x = x - 3 → 15 - 3 = 12
console.log(`Após x -= 3: ${x}`);

x *= 2;    // Atribuição de multiplicação: x = x * 2 → 12 * 2 = 24
console.log(`Após x *= 2: ${x}`);

x /= 4;    // Atribuição de divisão: x = x / 4 → 24 / 4 = 6
console.log(`Após x /= 4: ${x}`);

x %= 5;    // Atribuição de módulo (resto da divisão): x = x % 5 → 6 % 5 = 1
console.log(`Após x %= 5: ${x}`);

x **= 3;   // Atribuição de exponenciação: x = x ** 3 → 1 ** 3 = 1
console.log(`Após x **= 3: ${x}`);
